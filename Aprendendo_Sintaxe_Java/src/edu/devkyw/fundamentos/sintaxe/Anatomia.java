package edu.devkyw.fundamentos.sintaxe;

public class Anatomia {
    
    public static void main(String [] args){
        String nome = "Eduardo";
        int $idade = 22;
        System.out.println("Hello World !");
        System.out.println(nome);
        System.out.println($idade);
    }

    // Regras das variaveis
    // Devem conter apenas letras , underline , $ ou numeros 
    // Deve obrigatoriamente se iniciar por letras , underline ou $ , jamais com números
    // Deve iniciar com uma letra minuscula 
    // Não pode conter espaços
    // Não pode usar palavras-chave da linguagem
    // O nome deve ser único dentro de um escopo

}
